# Expressions.

# Arithmetic Expression 
a = (2 + 2) * (3 - 5 - 1)

# Modulo operator
b = 4 % 2

# Unary operator
c = -4

# Floor division
d = 3//2

# Shift operator
e = 4 >> 1

# Augumented assignment operators
f = 0
f += 2
f -= 1
f *= 3

# Comparison operators
g = (3 != 5) and (2 < 4) and (3 > 6) or (4 == 5)

# Chain comparisions
h = (1 < 2 < 3 < 4)

# Ternary operator
i = 0 if 4 > 2 else 1
j = 0 if 4 < 2 else 1 

# Power operator
k = 2 ** 3

print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)
print(i)
print(j)
print(k)